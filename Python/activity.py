A = float(input("Enter the 1st number: "))
B = int(input("Enter the 2nd number: "))

print(str(A) + " multiplied with " + str(B) + " is " + str(A * B))
print(str(A) + " divided by " + str(B) + " is " + str(A / B))
print(str(A) + " plus " + str(B) + " is " + str(A + B) + " and " + str(A) + " minus " + str(B) + " is " + str(A - B))
print(str(A) + " raised to the power of " + str(B) + " is " + str(A ** B))
